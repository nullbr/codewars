def decodeMorse(morseCode)
  dictionary = { '.-' => 'A', '-.' => 'N', '-...' => 'B', '---' => 'O', '-.-.' => 'C', '.--.' => 'P',
                 '-..' => 'D', '--.-' => 'Q', '.' => 'E', '.-.' => 'R', '..-.' => 'F', '...' => 'S',
                 '--..' => 'Z', '-' => 'T', '....' => 'H', '..-' => 'U', '..' => 'I', '...-' => 'V',
                 '.---' => 'J', '.--' => 'W', '-.-' => 'K', '-..-' => 'X', '.-..' => 'L', '-.--' => 'Y',
                 '--' => 'M', '.----' => '1', '-....' => '6', '..---' => '2', '--...' => '7', '...--' => '3',
                 '---..' => '8', '....-' => '4', '----.' => '9', '.....' => '5', '-----' => '0',
                 '...---...' => 'SOS', '-.-.--' => '!', '--.' => 'G', '--..--' => ',', '..--..' => '?',
                 '.-.-.-' => '.' }
  morseCode.split('  ').map do |word|
    word.split.map do |code|
      dictionary[code].nil? ? code : dictionary[code]
    end.join
  end.join(' ').strip
end

p decodeMorse('.... . -.--   .--- ..- -.. .')
p decodeMorse('-- --- ·-· ··· · -·-· --- -·· ·')
